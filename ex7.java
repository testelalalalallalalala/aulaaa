//Faça um programa que imprima na tela os valores no intervalo entre 10 e 200.

package aula1;
public class ex7 {
    public static void main(String[] args) {
        System.out.println("Números de 10 até 200:");
        
        for (int i = 10; i <= 200; i++) {
            System.out.println(i);
        }
    }
}

